	MOVWF	    PORTC
	MOVF	    BIRLER,W
	CALL	    LOOK_UP
	MOVWF	    PORTB
	CALL	    GECIKME
	
	MOVLW	    B'00000001'
	MOVWF	    PORTC
	MOVF	    ONLAR,W
	CALL	    LOOK_UP
	MOVWF	    PORTB
	CALL	    GECIKME
	DECFSZ	    DELAYY,1
	GOTO	    ISLEM1
	MOVLW	    D'2'
	ADDWF	    BIRLER,1
	GOTO	    BUTTON
		
ISLEM2
	CLRF	    BIRLER
	INCF	    ONLAR,1
	MOVLW	    D'10'
	SUBWF	    ONLAR,W
	BTFSS	    STATUS,Z
	GOTO	    BUTTON
	CLRF	    ONLAR
	GOTO	    BUTTON
	
	
	
