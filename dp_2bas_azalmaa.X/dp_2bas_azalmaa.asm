LOOK_UP
	ADDWF	    PCL,1
	RETLW	    0x3F
	RETLW	    0x06
	RETLW	    0x5B
	RETLW	    0x4F
	RETLW	    0x66
	RETLW	    0x6D
	RETLW	    0x7D
	RETLW	    0x07
	RETLW	    0x7F
	RETLW	    0x6F
;***********************************************************************************************
GECIKME	
	MOVLW	    0x0D
	MOVWF	    G1
	
	MOVLW	    0xFF
	MOVWF	    G2
LOOP
	DECFSZ	    G2,1
	GOTO	    LOOP
	
	DECFSZ	    G1,1
	GOTO	    LOOP
	
	RETURN
	
BASLA
	BANKSEL	    ADCON1
	MOVLW	    0x06
	MOVWF	    ADCON1
	
	BANKSEL	    TRISA
	MOVLW	    B'00001000'
	MOVWF	    TRISA
	
	BANKSEL	    PORTA
	CLRF	    PORTA
	BANKSEL	    TRISB
	CLRF	    TRISB
	
	BANKSEL	    PORTB
	CLRF	    PORTB
	
	BANKSEL	    TRISC
	CLRF	    TRISC
	
	BANKSEL	    PORTC
	CLRF	    PORTC

	CLRF	    ONLAR
	CLRF	    BIRLER

BUTTON
	BTFSS	    PORTA,3
	GOTO	    BUTTON
	GOTO	    DONGU
	
DONGU
	BCF	    STATUS,Z
	MOVLW	    D'10'
	MOVWF	    DELAYY
	
	MOVLW	    D'10'
	SUBWF	    BIRLER,W
	BTFSS	    STATUS,Z
	GOTO	    ISLEM1
	GOTO	    ISLEM2
	
ISLEM1
